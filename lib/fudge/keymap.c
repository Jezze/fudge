struct keymap *getkeymap(unsigned int type, unsigned int extended)
{

    switch (type)
    {

    case KEYMAP_US:
        return (extended) ? map_us_extended : map_us;

    case KEYMAP_SE:
        return (extended) ? map_se_extended : map_se;

    }

    return 0;

}

static void updatemodifier(struct keystate *keystate, unsigned int scancode)
{

    switch (scancode)
    {

    case 0x1D:
        keystate->mod |= KEYMOD_CTRL;

        break;

    case 0x9D:
        keystate->mod &= ~KEYMOD_CTRL;

        break;

    case 0x2A:
    case 0x36:
        keystate->mod |= KEYMOD_SHIFT;

        break;

    case 0xAA:
    case 0xB6:
        keystate->mod &= ~KEYMOD_SHIFT;

        break;

    case 0x38:
        keystate->mod |= KEYMOD_ALT;

        break;

    case 0xB8:
        keystate->mod &= ~KEYMOD_ALT;

        break;

    }

}

struct keycode *keymap_getkeycode(struct keystate *keystate, unsigned int type, unsigned int scancode)
{

    if (scancode == 0xE0)
    {

        keystate->extended = !keystate->extended;

        return 0;

    }

    else
    {

        struct keymap *keymap = getkeymap(type, keystate->extended);

        updatemodifier(keystate, scancode);

        if (keystate->mod & KEYMOD_SHIFT)
            return &keymap[scancode].keycode[1];

        return &keymap[scancode].keycode[0];


    }

}

