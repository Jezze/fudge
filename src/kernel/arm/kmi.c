void kmi_setup(void)
{

    for (;;)
    {

        unsigned char x;

        while (!(reg_read32(KMI_REG_KBD_STATUS) & KMI_STATUS_FULL));

        x = reg_read8(KMI_REG_KBD_DATA);

        if (x < 0x80)
        {

            uart_putc(mapUS[x]);

        }

    }

}

